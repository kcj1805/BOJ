import java.io.*;
import java.util.*;

public class BOJ2742 {
    //기찍 N
    public static void main(String[] args) throws IOException
    {
        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        sc.close();

        for(int i=N;i>0;i--)
        {
            System.out.println(i);
        }
    }
}
